<?php

namespace App\DataTables;

use App\Models\City;
use App\Models\Driver;
use App\Models\DriverStatus;
use Yajra\DataTables\Services\DataTable;

class DriverStatusDatatable extends DataTable
{
    protected $tableId;

    public function __construct($tableId = 'driver-status-table')
    {
        $this->tableId = $tableId;
    }

    public function dataTable($query)
    {
        return datatables()
            ->eloquent($query)
            ->addColumn('action', function ($item) {
                $view = view('driver_statuses.action', [
                    'item' => $item
                ])->render();
                return $view;
            })
            ->addIndexColumn()
            
            ->rawColumns(['action', 'status']);
    }

    public function query(DriverStatus $model)
    {
        $query = $model->newQuery()->orderBy('id', 'desc');
        return $query;
    }

    public function html()
    {
        return $this->builder()
            ->setTableId('driver-status-table')
            ->columns($this->getColumns())
            ->minifiedAjax()
            ->parameters([
                'paging' => true,
                'info' => false,
                'searching' => true,
                'ordering' => false,
                'responsive' => true,
                'autoWidth' => false,
                'scrollX' => true,
                'scrollY' => '',
                'pageLength' => 100,
                'buttons' => [
                    ['extend' => 'colvis', 'text' => 'Sütunları Göstər/Gizlə']
                ]
            ])
            ->dom('Bfrtip')
            ->orderBy(0);
    }

    protected function getColumns()
    {
        return [
            ['data' => 'DT_RowIndex', 'title' => 'No:', 'orderable' => false, 'searchable' => false],
            ['data' => 'status', 'title' => 'Ad'],
            ['data' => 'action', 'title' => 'Action', 'exportable' => false, 'printable' => false, 'orderable' => false, 'searchable' => false],
        ];
    }


}
